int		ft_is_prime(int nb)
{
	int i;

	i = 2;
	while (nb % i != 0 && i <= nb)
		i++;
	return (nb > 0 && nb != 1 && (nb == 2 || nb % 2 != 0)
			&& (nb % i != 0 || nb == i) ? 1 : 0);
}
