char	*ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i])
		i++;
	while (src[i])
	{
		src[i] = dest[j];
		i++;
		j++;
	}
	dest[j] = '\0';\
	return (dest);
}
